user_input = input("Enter either 'A', 'B', or 'C': ")

if user_input == 'A':
    print('Apple')
else:
    if user_input == 'B':
        print('Banana')
    else:
        if user_input == 'C':
            print('Coconut')
        else:
            print("Invalid input. Please enter either 'A', 'B', or 'C'.")
